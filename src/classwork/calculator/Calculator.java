package classwork.calculator;

public class Calculator {
    double plus(double x, double y) {

        double z;
        z = x + y;
        return z;
    }

    double minus(double x, double y) {
        double z;
        z = x - y;
        return z;
    }

    double multiply(double x, double y) {
        double z;
        z = x * y;
        return z;
    }

    double divide(double x, double y) {
        double z;
        z = x / y;
        return z;
    }

}
